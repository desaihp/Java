package com.ControlStatements.task;

public class Switch {

	public static void main(String[] args) {
		int number = 20;

		switch (number) {

		case 1:
			System.out.println("number is 10");
			break;
		case 2:
			System.out.println("number is 20");
			break;
		case 3:
			System.out.println("number is 30");
            break;
		default:
			System.out.println("not in 10,20 or 30");
            break;
		}

	}

}
